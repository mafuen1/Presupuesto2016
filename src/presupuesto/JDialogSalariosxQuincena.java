    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
      try {
                Utilities utils = new Utilities ();
                PresupuestoDAO con = PresupuestoDAO.getInstance();                                
                
                int rowArr[] = jTable1.getSelectedRows();
                
                if (rowArr.length ==0)        
                {
                JOptionPane.showMessageDialog(null,
                            "Debe seleccionar un rubro de la tabla.");
                }          
                
                for (int i=0; i<rowArr.length ; i++){
                if  (rowArr[i]!=-1)
                {                                
                    String salario = jTable1.getValueAt(rowArr[i], 1).toString();
                    String motivo = jTable1.getValueAt(rowArr[i], 2).toString();

                    if(JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar el salario seleccionado?",
                                                            "Warning",
                                                            JOptionPane.OK_CANCEL_OPTION,
                                                            JOptionPane.QUESTION_MESSAGE)
                                                            ==JOptionPane.OK_OPTION){                                                
                       
                             salario = utils.priceWithoutCommas(salario);
                            con.borrarSalario(this.presupuesto.getIdpresupuesto(),motivo, Double.parseDouble(salario));
                           con.modificarMontoDisponible(this.presupuesto.getIdpresupuesto());     
                        
                    }
                }
                }                                                    
            } catch (Exception ex) {
                DialogError error = new DialogError (null,true,ex);
                error.setVisible(true);
            }     
            cargar();   
    }//GEN-LAST:event_jButton2ActionPerformed

    
    public void btn_modificar(){
        TableModel model = jTable1.getModel();
        Utilities utils = new Utilities();
        
        
        int row = jTable1.getSelectedRow();
        int col = jTable1.getSelectedColumn();  

        if  (row!=-1 && col!=-1)
        {                                
            try {
                String idusuario = jTable1.getValueAt(row, 0).toString();
                String salario = utils.priceWithoutCommas( jTable1.getValueAt(row, 1).toString());
                String motivo = jTable1.getValueAt(row, 2).toString();
                
                
                JDialogInsertarSalario s = new JDialogInsertarSalario (null,true,this.presupuesto,idusuario, salario,motivo);
                s.setVisible(true);
                cargar();
            } catch (ParseException ex) {
                Logger.getLogger(JDialogSalariosxQuincena.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(null,
            "Debe seleccionar un rubro de la tabla.");
        }     
    }
    

public void cargar()
{
    try {
            // TODO add your handling code here:              
        PresupuestoDAO con = PresupuestoDAO.getInstance();                                                            
        
        this.rowData=con.getSalariosQuincena(Integer.parseInt(this.presupuesto.getIdpresupuesto()));
        actualizarTabla(this.rowData);       
                
        Utilities utils = new Utilities();
                
        if(rowData!=null){
            int registros = rowData.length;
            double tmp_monto,monto=0;
            for (int i=0; i<registros; i++)
            {                
                String str_monto = (String) rowData [i][1];
                
                try {                   
                    tmp_monto = Double.parseDouble(utils.priceWithoutCommas(str_monto));                    
                    monto = monto + tmp_monto;                                                            
                } catch (ParseException ex) {
                    ex.printStackTrace();
                }                             
            } 
        
        lbl_monto.setText(utils.priceWithDecimal(monto));
    }
   
    } catch (Exception ex) {
                DialogError error = new DialogError (null,true,ex);
               error.setVisible(true);
    }    
}


